package com.mindforger.coachingnotebook.server.admin;

public class AdminReport {
	
	int users;
	int grows;
	int questions;
	int checkItems;
	
	public AdminReport() {
	}

	public int getUsers() {
		return users;
	}

	public void setUsers(int users) {
		this.users = users;
	}

	public int getGrows() {
		return grows;
	}

	public void setGrows(int grows) {
		this.grows = grows;
	}

	public int getQuestions() {
		return questions;
	}

	public void setQuestions(int questions) {
		this.questions = questions;
	}

	public int getCheckItems() {
		return checkItems;
	}

	public void setCheckItems(int checkItems) {
		this.checkItems = checkItems;
	}
}
